# 1. Arithmetic Operators:

# + ==>Addition
# - ==>Subtraction
# * ==>Multiplication
# / ==>Division operator
# % ===>Modulo operator

# // ==>Floor Division operator

# ** ==>Exponent operator or power operator 


a=10
b=20

print('a+b=',a+b)

print('a+b=',a-b)

print('a*b=',a*b)

print('a//b=',a/b)

print('a//b=',a//b)

print('a**b=',a%b)

print('a**b=',a**b)
